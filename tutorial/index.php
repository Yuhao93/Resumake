            <section id="profile"><div class="page-header">
                <h1>Your Profile <small>Keep Track of Everything</small></h1>
            </div></section>
            <p>Your profile is the page you see when you are logged in. This is, unlike those from many social networking sites, private and only visible to you. The point of it being private is so that you can manage different resumes and ensure that companies only see what you want them to see.</p>
            <p>Here, you can perform trivial tasks such as selecting a profile picture or editing your personal information. If you have chosen a profile picture, companies will be able to see it. Editing your personal information allows part of your resume to be automatically filled in with information such as your email or where you live. This can save time in the long run and you can always edit each resume to display something different.</p>
            <p>The resume section that takes up the majority of your screen is where you can add, delete, and modify your resumes. You can create different resumes for different purposes and when our match-making functionality goes into effect, you can specify which resumes are visible for which searches.</p>
            <br><br><br>
        </div>
        
        <div class="row span10">
            <section id="make"><div class="page-header">
                <h1>Making A Resume <small>Resumes Made Easy</small></h1>
            </div></section>
            <p>In your profile, click the "Add A Resume" button to go to the resumaker page. On this page is a form that, after filling out, will be used to automatically generate your resume webpage. The information will be broken up into different sections that pertain to the different parts of the resume</p>
            <p>As of now, there are six parts of your resume. Each is explained in more detail further down the page. Future releases may include custom sections or different templates for your resume.</p>
            <br><br><br>
        </div>
        
        <div class="row span10">
            <section id="linkedin"><div class="page-header">
                <h1>Importing Your LinkedIn Profile <small>To Make Easy Easier</small></h1>
            </div></section>
            <p>An exciting new functionality is the ability to generate your resume webpage simply by connecting to your LinkedIn profile.</p>
            <p>You can, with a click of a button, import your profile so that all of the information you already have stored in your LinkedIn profile doesn't have to be entered in again. On the resume maker screen, click the "Login to LinkedIn" button to import your LinkedIn profile.</p>
            <br><br><br>
        </div>
        
        <div class="row span10">
            <section id="basic"><div class="page-header">
                <h1>Your Basic Information <small>Who You Are</small></h1>
            </div></section>
            <p>The first part of the form consists of your basic information. This is where your name and other one-liners go.</p>
            <ul>
            <li>The Resume field will be where the name of your resume goes (e.g. My Resume, Joe's Computer Science Resume, Resume #1). This is purely for your organization only as no one else will be able to see your resume title.</li>
            <li>The Name field (which should be automatically filled in for you) is where your name goes. You are free to change your name to anything you want, just understand that this is what anyone who looks at your resume will see first.</li>
            <li>The Position field is what you would introduce yourself as (e.g. Computer Science Engineer, Hobbyist, Free-Lance Painter, Environmentalist, Dragon-slayer). This should accurately summarize what you do and how you want to appear to the companies who view your resume.</li>
            <li>The Statement field is a short one to two sentences that clearly display your intent. This is where you explicitly say what you're looking for in a job (e.g. Seeking a part-time position as a software developer. or Interested in a full-time position in a research facility.)</li>
            </ul>
            <br><br><br>
        </div>
        
        <div class="row span10">
            <section id="contact"><div class="page-header">
                <h1>Your Contact Information <small>How People Get a Hold Of You</small></h1>
            </div></section>
            <p>This part of the resume is where you list your contact information, if you filled out the personal information in your profile, the entire sections should be automatically filled in for you. You are, of course, free to change anything.</p>
            <ul>
            <li>The Address field is your physical address. It should contain information such as your street and house/apartment number.</li>
            <li>The City field is the city or town you live in.</li>
            <li>The State field is for which state you live in. A user who does not live in the United States should leave it blank and place all the information in the address field.</li>
            <li>The Zipcode field is for the zipcode or area code that you live in.</li>
            <li>The Phone Number field is for your phone number. This is the phone number that you would prefer to be contacted on.</li>
            <li>The Email field is for your email. This is the email address that you would prefer to be contacted on.</li>
            </ul>
            <br><br><br>
        </div>
        
        <div class="row span10">
            <section id="education"><div class="page-header">
                <h1>Your Education <small>Which Schools You've Attended</small></h1>
            </div></section>
            <p>The Education part of the resume contains all the information regarding your education experience.</p>
            <p>In this section, you can add multiple schools to your education history by clicking the "Add Education" button. Upon clicking it, a window should appear where you can fill in all of the information for a school.</p>
            <p>After you save this education, it will appear in the form. Clicking on the form will re-open the window so you can edit the education</p>
            <ul>
            <li>The School field is the name of the school, institution, university or college that you attended. It should be clear enough so that anyone will be able to understand it (abbreviating it into two or three letters might not be a good idea here.)</li>
            <li>The Degree field is the degree that you earned or will earn at this school. Again, this should be fairly clear and indicative of what you have worked towards or what you are currently working towards.</li>
            <li>The Start Date field is the date that you started at this school. This should always be filled in even if you have not started here yet. If you have yet to start at this school, place the expected start date.</li>
            <li>The End Date field is the date that you will have finished your education at this school. Even if this hasn't happened yet, you can put an expected finish date. In leaving it blank, the finish date will be replaced by the text "Ongoing"</li>
            <li>You can add achievements or special recognitions pertaining to your university (Suma Cum Laude, Dean's Honor List, etc.) by clicking on the "Add an Award" button. In the resulting field that appears, enter in the name of the award and click "Add".</li>
            </ul>
            <br><br><br>
        </div>
        
        <div class="row span10">
            <section id="skills"><div class="page-header">
                <h1>Your Skills <small>Anything About You That's Interesting</small></h1>
            </div></section>
            <p>The Skills part of the resume is where you list your any of your pertinent skills. Pertinent skills is a broad description that includes almost anything that will make a recruiter consider hiring you. Listing less-pertinent or irrevelant skills may be beneficial, but it might also clutter up your resume and make it harder for recruiters to spot the crucial skills.</p>
            <p>Skills are broken up into catgories so that you and the recruiter can view your skills in an organized and neat fashion.</p>
            <p>To add a category, click the "Add A Category" button. A window will appear where you will be able to enter in information related to the category which you are creating.</p>
            <p>After you save the category, you can edit it by clicking on the category name in the form.</p>
            <ul>
            <li>The Category field will have the name of the category you are creating. It should be somewhat broad yet it should also give a good understanding of how your skills in this category fit together.</li>
            <li>To add a skill, click on the "Add A Skill" button. In the following fields that appear, fill in the name of your skill and a brief description of your skill. When you are ready to add your skill, click the "Add" button.</li>
            </ul>
            <br><br><br>
        </div>
        
        <div class="row span10">
            <section id="experience"><div class="page-header">
                <h1>Your Experience <small>What You've Done</small></h1>
            </div></section>
            <p>The Experience part of your resume will list the previous experience you have had working in your field. This could include internships, research, free-lance projects and hobbies, full-time positions, and part-time positions. Essentially, anything that you can use to show that you've had experience in your field goes here.</p>
            <p>To add an experience, click the "Add An Experience" button. A window will appear where you will be able to enter in information related to the experience which you are adding.</p>
            <p>After you save the experience, you can edit it by clicking on the experience name in the form.</p>
            <ul>
            <li>The Position field is the name of your current or most recent position for this experience. It is how you would introduce yourself. (e.g. Cashier, Data Mining Intern, Part-Time Researcher in Particle Physics, Project Manager, etc.)</li>
            <li>The Start Date field is the date which you started. Even if you haven't started yet, you should still enter the expected start date.</li>
            <li>The End Date field is the date which you stopped working with this group. If you are still working at this group, you can leave the field blank so that it will display "Ongoing" or you can enter your expected end date.</li>
            <li>The Group field is the group (company or organization) which you worked for. (e.g. Free-Lance, Target, Google, etc.)</li>
            <li>You can add various facts and links to your experience. A fact is a brief chunk of information that describes what you did at the company in more detail. You can add multiple facts together to create a description of your position. If you believe that a link to a webpage could better explain your position, you can add a link as well.</li>
            <li>To add a fact, click on the "Add A Fact" button. In the resulting field, enter the fact into the text area and then press the "Add" button.</li>
            <li>To add a link, click on the "Add A Link" button. In the resulting fields, enter the link name(what people will see on the resume) and the link (the address of the web page) and then press the "Add" button.</li>
            </ul>
            <br><br><br>
        </div>
        
        <div class="row span10">
            <section id="activities"><div class="page-header">
                <h1>Your Activities <small>For Everything Else</small></h1>
            </div></section>
            <p>The Activity part of your resume will list the parts of your life that do not fit into the above categories. Keep in mind that only relevant information that would convince a recruiter you are a right fit for the job should be included.</p>
            <p>To add an activity, click the "Add An Activity" button. A window will appear where you will be able to enter in information related to the activity which you are adding.</p>
            <p>After you save the activity, you can edit it by clicking on the activity name in the form.</p>
            <ul>
            <li>The Position field is the name of your current or most recent position for this activity. It is how you would introduce yourself. (e.g. Treasurer, Captain, Hobbyist, Volunteer, etc.)</li>
            <li>The Start Date field is the date which you started. Even if you haven't started yet, you should still enter the expected start date.</li>
            <li>The End Date field is the date which you left this activity. If you are still participating in this activity, you can leave the field blank so that it will display "Ongoing" or you can enter your expected end date.</li>
            <li>The Group field is the group that is associated with this activity. (e.g. Boy Scouts, Student Council, GreenPeace, etc.)</li>
            <li>You can add various facts and links to your activity. A fact is a brief chunk of information that describes what you did at this activity in more detail. You can add multiple facts together to create a description of your position. If you believe that a link to a webpage could better explain your position, you can add a link as well.</li>
            <li>To add a fact, click on the "Add A Fact" button. In the resulting field, enter the fact into the text area and then press the "Add" button.</li>
            <li>To add a link, click on the "Add A Link" button. In the resulting fields, enter the link name(what people will see on the resume) and the link (the address of the web page) and then press the "Add" button.</li>
            </ul>
